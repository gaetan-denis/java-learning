public class Main {
    public static void main(String[] args){
        

        // L'opérateur d'affectation

        int monPremierEntier = 42; 

        System.out.println("Mon premier entier : " + monPremierEntier);

        /**
         * opérateur d’affectation par addition:
         * - Ajoute la valeur de droite à la variable de gauche.
         * - Réaffecte le résultat à cette variable.
         */

        monPremierEntier += 12;

        System.out.println("Mon premier entier : " + monPremierEntier);
    }
}